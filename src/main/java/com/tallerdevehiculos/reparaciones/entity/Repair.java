package com.tallerdevehiculos.reparaciones.entity;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;
import java.io.Serializable;
import java.util.Set;

@Data
@AllArgsConstructor
@NoArgsConstructor
@DynamoDBTable(tableName = "repair")
public class Repair implements Serializable{
    @DynamoDBAutoGeneratedKey
    @DynamoDBHashKey(attributeName = "rId")
    private String rId;
    @DynamoDBAttribute
    private String repairDate;
    @DynamoDBAttribute
    private String repairBy;
    @DynamoDBAttribute
    private String licensePlateVehicle;
    @DynamoDBAttribute
    private String state;
    @DynamoDBAttribute
    private Set<String> listStates;
    @DynamoDBAttribute
    private Set<String> spareParts;
    @DynamoDBAttribute
    private int cost;
    @DynamoDBAttribute
    private Set<Integer> partsCost;
    @DynamoDBAttribute
    private int otherCosts;
    @DynamoDBAttribute
    private String inCharge;

    public String getrId() {
        return rId;
    }

    public void setrId(String rId) {
        this.rId = rId;
    }

    public String getRepairDate() {
        return repairDate;
    }

    public void setRepairDate(String repairDate) {
        this.repairDate = repairDate;
    }

    public String getRepairBy() {
        return repairBy;
    }

    public void setRepairBy(String repairBy) {
        this.repairBy = repairBy;
    }

    public String getLicensePlateVehicle() {
        return licensePlateVehicle;
    }

    public void setLicensePlateVehicle(String licensePlateVehicle) {
        this.licensePlateVehicle = licensePlateVehicle;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public Set<String> getListStates() {
        return listStates;
    }

    public void setListStates(Set<String> listStates) {
        this.listStates = listStates;
    }

    public Set<String> getSpareParts() {
        return spareParts;
    }

    public void setSpareParts(Set<String> spareParts) {
        this.spareParts = spareParts;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }

    public Set<Integer> getPartsCost() {
        return partsCost;
    }

    public void setPartsCost(Set<Integer> partsCost) {
        this.partsCost = partsCost;
    }

    public int getOtherCosts() {
        return otherCosts;
    }

    public void setOtherCosts(int otherCosts) {
        this.otherCosts = otherCosts;
    }

    public String getInCharge() {
        return inCharge;
    }

    public void setInCharge(String inCharge) {
        this.inCharge = inCharge;
    }
}


